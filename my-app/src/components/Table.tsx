import React from 'react';

interface TableProps {
  rows: string[][];
}

const Table: React.FC<TableProps> = ({ rows }) => {
  return (
    <table className="table">
      <thead>
        <tr>
          <th>Row name</th>
          <th>Column 1</th>
          <th>Column 2</th>
          <th>Column 3</th>
          <th>Column 4</th>
          <th>Column 5</th>
          <th>Column 6</th>
        </tr>
      </thead>
      <tbody>
        {rows.map((row, index) => (
          <tr key={index}>
            {row.map((cell, cellIndex) => (
              <td key={cellIndex}>{cell}</td>
            ))}
          </tr>
        ))}
      </tbody>
    </table>
  );
};

export default Table;